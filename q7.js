/* **Precedence and Associativity in JavaScript:**

1. **Precedence:**
   - Determines the order in which operators are evaluated in an expression.
   - Higher precedence operators are evaluated first.
   - Example: In `2 + 3 * 4`, multiplication (`*`) has higher precedence than addition (`+`), so `3 * 4` is evaluated first.

2. **Associativity:**
   - Deals with the order of evaluation when operators with the same precedence appear in an expression.
   - Left-associative operators evaluate from left to right.
   - Right-associative operators evaluate from right to left.
   - Example: In `5 - 3 - 1`, the subtraction operator is left-associative, so `5 - 3` is evaluated first.

**Importance:**
Understanding precedence and associativity is crucial for writing correct and predictable expressions.
 It helps in avoiding errors and ensures that expressions are evaluated in the intended order.
 Explicitly using parentheses can also clarify code and override default precedence and associativity rules.
 Mastery of these concepts enhances code readability and prevents unexpected behavior in complex expressions,
  making code more maintainable and less error-prone.
  */